function newdata()
{
	jQuery.ajax({'url':'<?php echo Yii::app()->createUrl('hr/position/create')?>','data':{},
		'type':'post','dataType':'json',
		'success':function(data) {
			if (data.status == "success")
			{
			$("input[name='position_0_positionid']").val(data.positionid);
			$("input[name='position_0_positionname']").val('');
      $("input[name='position_0_recordstatus']").prop('checked',true);
			
			$('#InputDialog').modal();
			}
			else
			{
				toastr.error(data.div);
			}
		},
		'cache':false});
	return false;
}

function updatedata($id)
{
	jQuery.ajax({'url':'<?php echo Yii::app()->createUrl('hr/position/update')?>','data':{'id':$id},
		'type':'post','dataType':'json',
		'success':function(data) {
			if (data.status == "success")
			{
				$("input[name='position_0_positionid']").val(data.positionid);
				$("input[name='position_0_positionname']").val(data.positionname);
      if (data.recordstatus == 1)
			{
				$("input[name='position_0_recordstatus']").prop('checked',true);
			}
			else
			{
				$("input[name='position_0_recordstatus']").prop('checked',false)
			}
				
				$('#InputDialog').modal();
			}
			else
			{
				toastr.error(data.div);
			}
		},
		'cache':false});
	return false;
}

function savedata()
{
var recordstatus = 0;
	if ($("input[name='position_0_recordstatus']").prop('checked'))
	{
		recordstatus = 1;
	}
	else
	{
		recordstatus = 0;
	}
	jQuery.ajax({'url':'<?php echo Yii::app()->createUrl('hr/position/save')?>',
		'data':{
			'positionid':$("input[name='position_0_positionid']").val(),
			'positionname':$("input[name='position_0_positionname']").val(),
      'recordstatus':recordstatus,
		},
		'type':'post','dataType':'json',
		'success':function(data) {
			if (data.status == "success")
			{
				$('#InputDialog').modal('hide');
				toastr.info(data.div);
				$.fn.yiiGridView.update("GridList");
			}
			else
			{
				toastr.error(data.div);
			}
		},
		'cache':false});
}


